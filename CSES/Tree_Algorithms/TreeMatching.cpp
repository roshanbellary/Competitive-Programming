const int MN = 2e5;
int N, dp[MN][2];
vector<int> l[MN];
void dfs(int i, int p){
    dp[i][0]=dp[i][1]=0;
    bool ft = 0;
    for (int& j:l[i]){
        if (j==p) continue;
        dfs(j,i);dp[i][0]+=max(dp[j][0],dp[j][1]);
        if (dp[j][0]+1>dp[j][1] && !ft) dp[i][1]++, ft=1;
    }
    dp[i][1]+=dp[i][0];
}
