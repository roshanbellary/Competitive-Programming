const int MN = 100, MS = 1e4+1;
int T, N, a[MN], b[MN];
bool r[MS][MN];
void solve(){
    rv(N);
    for (int i=0;i<N;i++) rv(a[i]);
    for (int i=0;i<N;i++) rv(b[i]);
    if (N==1){cout << "0\n"; return;}
    for (int i=0;i<N;i++) for (int j=0;j<MS;j++) r[j][i]=0;
    r[a[0]][0]=r[b[0]][0]=1;
    for (int i=1;i<N;i++){
        for (int j=0;j<MS-b[i];j++) if (r[j][i-1]) r[j+b[i]][i]=1;
        for (int j=0;j<MS-a[i];j++) if (r[j][i-1]) r[j+a[i]][i]=1;
    }
    ll ov = 0, sum = 0;
    for (int i=0;i<N;i++) ov+=a[i]*a[i], ov+=b[i]*b[i], sum+=(a[i]+b[i]);
    ov*=(N-2);
    ll minv = 1e18;
    for (int i=0;i<MS;i++) if (r[i][N-1]) minv=min(i*i+(sum-i)*(sum-i),minv);
    ov+=minv;
    cout << ov << "\n";
}
