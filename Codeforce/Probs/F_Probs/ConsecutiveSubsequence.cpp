const int MN = 2e5;
struct Node{int v=-1;};
int N, pi[MN], v[MN], dp[MN], MV = 0, ind = -1;
map<int,Node> m;vector<int> res;
int main(){
	setIO();rv(N);
    for (int i=0;i<N;i++) rv(v[i]);
    for (int i=0;i<N;i++){
        if (m[v[i]-1].v<0) pi[i]=-1;
        else pi[i]=m[v[i]-1].v;
        m[v[i]].v=i; 
    }
    dp[0]=1;
    for (int i=1;i<N;i++){
        dp[i]=1;
        if (pi[i]<0) continue;
        else dp[i]=(dp[pi[i]]+1);
    }
    for (int i=0;i<N;i++) if (MV<dp[i]) ind=i,MV=dp[i];
    cout << MV << "\n";
    while (true){
        res.pb(ind+1);
        if (pi[ind]<0) break;
        else ind=pi[ind];
    }
    reverse(res.begin(),res.end());
    for (int& j:res) cout << j << " ";
    cout << "\n";
}
