const int MN = 100, MT = 25001;
struct Ride{
    ll a, b, t;
    bool sort(const Ride &n) const{
        return (b<n.b);
    }
};
int N, Q;
Ride r[MN];
ll dp[MT];
int main(){
	setIO();rv(N);
    for (int i=0;i<N;i++){
        rv(r[i].a,r[i].b,r[i].t);
    }
    sort(r,r+N);
    for (int i=0;i<N;i++){
        for (int t=MT-r[i].t-1;t>=0;t--){
            ll c = 1;
            ll col = 0;
            while (1){
                if (t+c*r[i].t>=MT || (r[i].a-r[i].b*(c-1)*(c-1))<=0) break;
                col+=(r[i].a-r[i].b*(c-1)*(c-1));
                dp[t+c*r[i].t]=max(dp[t+c*r[i].t], dp[t]+col);
                c++;
            }
        }
    }
    for (int i=0;i<MT;i++) if (i) dp[i]=max(dp[i],dp[i-1]);
    rv(Q);
    while (Q--){
        int T; rv(T);
        cout << dp[T] << "\n";
    }
}
